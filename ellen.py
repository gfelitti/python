def falaellen(fala):
    if fala == "Gigi":
        print ("Oi")
    elif fala == "Cê me ama?":
        print ("Amo")
    elif fala == "Ama muito?":
        print ("Muito")
    else:
        print ("Coisinha linda, fala de novo")
    return

falaellen("Gigi")
falaellen("Cê me ama?")
falaellen("Coisinha linda")
falaellen("Ama muito?")
